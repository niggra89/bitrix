<p>Die Lieferung erfolgt an die Adresse, die bei der Bestellung angegeben wurde. Möchten Sie, dass die bestellte Ware an eine andere Adresse geliefert wird, teilen Sie diese Adresse unserem Versandservice mit.</p>

