<b>Warum "Möbel"?</b>
<p>"Möbel" ist der größte Möbel-Shop im Land: Wir haben über 50 Filialen, welche sich in über 30 Städten befinden. In jeder Filiale stehen ein professionell
 ausgebildetes Team und notwendige Infrastruktur dafür, dass Sie als unsere Kunden gewünschte Produkte schnell finden, einfach erwerben und rechtzeitig geliefert 
 bekommen, egal, ob Sie ein Möbelstück für Ihr Schlafzimmer oder für Ihre Küche suchen.
Wir stehen für Auswahl und Qualität. Mit uns können Sie Ihr Zuhause schön und gemütlich einrichten und gestalten.</p>
<b>Warum online einkaufen?</b>
<p>"Möbel" ist einer der ersten Online-Shops, welcher sich zudem speziell auf Möbelherstellung und -Lieferung spezialisiert und auch den kompletten Service rund
 um das Thema Möbel anbietet. Seit vielen Jahren haben wir unsere Erfahrungen akkumuliert, damit Sie jetzt auf unserer Website alles bestellen können. Und zwar 
 schnell und einfach.
Ihr Kauf wird jedes Mal mit einer E-Mail bestätigt, so dass Sie einen schriftlichen Eintrag über den Kauf haben. Sie haben außerdem die Möglichkeit, die Ausführung
 Ihrer Bestellung online zu verfolgen.
Die Website "Möbel für Sie" verwendet bei der Online-Bezahlung nur modernste Verschlüsselungstechniken. Ihre Kartendaten werden daher vor jeglichem Missbrauch 
geschützt.</p>