<p><b>Widerrufsbelehrung</b></p>

<p><b>Widerrufsrecht</b><br />
Sie können Ihre Vertragserklärung innerhalb von (zwei Wochen) ohne Angabe von Gründen in Textform (z. B.
Brief, Fax, E-Mail) (oder durch Rücksendung der Sache) widerrufen. Die Frist beginnt frühestens mit Erhalt dieser Belehrung. Zur Wahrung der Widerrufsfrist genügt die rechtzeitige Absendung des Widerrufs (oder der Sache).</p>

<p>Der Widerruf ist zu richten an:</p>

<p><b>Widerrufsfolgen</b><br />
Im Falle eines wirksamen Widerrufs sind die beiderseits empfangenen Leistungen zurückzugewähren (und ggf. gezogene Nutzungen (z. B. Zinsen) herauszugeben). Können Sie uns die empfangene Leistung ganz oder teilweise nicht oder nur in verschlechtertem Zustand zurückgewähren, müssen Sie uns insoweit ggf. Wertersatz leisten.<br />
(Bei der Überlassung von Sachen gilt dies nicht, wenn die Verschlechterung der Sache ausschließlich auf deren Prüfung - wie sie Ihnen etwa im Ladengeschäft möglich gewesen wäre - zurückzuführen ist. Im Übrigen können Sie die Wertersatzpflicht vermeiden, indem Sie die Sache nicht wie ihr Eigentum in Gebrauch nehmen und alles unterlassen, was deren Wert beeinträchtigt. Paketversandfähige Sachen sind (auf unsere Kosten und Gefahr) zurückzusenden.<br />
Nicht paketversandfähige Sachen werden bei Ihnen abgeholt.) Verpflichtungen zur Erstattung von
Zahlungen müssen Sie innerhalb von 30 Tagen nach Absendung Ihrer Widerrufserklärung erfüllen.</p> 


