?><p>Der Einkauf in unserem E-Shop ist einfach, er besteht nur aus einigen wenigen Schritten.</p>
 
<h2>1. Bestellung eingeben</h2>

<p>Wählen Sie eine Ware aus und klicken Sie dann auf <b>In den Warenkorb legen</b> - die Ware wird Ihrem Warenkorb hinzugefügt.</p>

<p>Wenn Sie mit der Warenauswahl fertig sind, klicken Sie auf <b>Ihr Warenkorb</b>.</p>

<p>Auf der Seite <b>Ihr Warenkorb</b> finden Sie eine Auflistung aller von Ihnen ausgewählten Waren mit einer kurzen Beschreibung.</p>

<h2>2. Bestellung bestätigen</h2>

<p>Nach Eingabe der erforderlichen Informationen über den Käufer (Vorname, Nachname, Lieferadresse, Kontaktdaten, gewünschte Lieferzeit etc.) klicken Sie auf <b>Bestellen</b>.</p>

<p>Eine Kopie der Bestellung wird Ihnen unverzüglich an die bei der Bestellung angegebene E-Mail-Adresse gesendet.</p>
 
<p><b>Beachten Sie</b>, dass eine falsch angegebene Telefonnummer oder eine nicht vollständig angegebene Adresse zu Verzögerungen führen können. Prüfen Sie bitte sorgfältig Ihre persönlichen Daten, bevor Sie Ihre Bestellung abschicken. </p>

<p>Nachdem Sie Ihre Bestellung abgeschickt haben, werden unsere Mitarbeiter mit Ihnen Kontakt aufnehmen, um die Lieferzeit und eventuell andere Details zu besprechen. Wenn Sie die Ware per Banküberweisung bezahlen, wird Ihnen vorab eine elektronische Rechnung an Ihre E-Mail-Adresse gesendet. </p>

<p><b>Anmerkung</b>: Kunden, die häufiger bei uns einkaufen, können sich auf der Website registrieren, ihre Daten einmalig eingeben, und dann bei jeder zukünftigen Bestellung durch ihr persönliches Nutzerkonto auf die eigenen Daten automatisch zugreifen. </p>

<h2>3. Zahlungsmöglichkeiten und Preise</h2>

<p>Alle Preise auf der Website verstehen sich als Endpreise inklusive aller Steuern.</p>

<p><b>Beachten Sie bitte</b>, dass bei jeder einzelnen Bestellung nur eine von Ihnen ausgewählte Zahlungsart möglich ist. Teilzahlungen mit verschiedenen Zahlungsarten für eine Bestellung sind nicht erlaubt.</p>

<p>Mögliche Zahlungsarten:</p>

<ul>
  <li><b>Barzahlung</b>. 
    <br />
  Sie bezahlen die Ware direkt an den Mitarbeiter des Versandservices bei der Lieferung der Ware. Mit der Ware bekommen Sie auch die Rechnung und Garantiescheine.</li>

  <li><b>Banküberweisung</b>. 
    <br />
   Diese Zahlungsart ist gut geeignet für Unternehmen. Nach Bestellung wird Ihnen eine Rechnung per E-Mail zugesendet. Nach Zahlungseingang erfolgt die Lieferung an die angegebene Adresse. </li>

  <li><b>Bezahlung via Paypal</b>. 
    <br />
  Schnellste Möglichkeit, die Ware geliefert zu bekommen.</li>
</ul>
